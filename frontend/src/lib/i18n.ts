type UiMessageKey = keyof (typeof uiMessages)['tw'];

const localeStore = writable<OfficialStatLocale>('tw');

const normalizeLocale = (value: string | null | undefined): OfficialStatLocale | undefined => {
  if (!value) {
    return undefined;
  }

  const lowered = value.trim().toLowerCase();
  if ((officialStatLocales as readonly string[]).includes(lowered)) {
    return lowered as OfficialStatLocale;
  }

  return localeAliases.find(([pattern]) => pattern.test(lowered))?.[1];
};

const detectLocaleFromNavigator = (): OfficialStatLocale => {
  if (!isBrowser) {
    return 'tw';
  }

  const candidates = [...(navigator.languages || []), navigator.language];
  for (const candidate of candidates) {
    const normalized = normalizeLocale(candidate);
    if (normalized) {
      return normalized;
    }
  }

  return 'en';
};

export const locale = {
  subscribe: localeStore.subscribe
};

export const uiLocale = derived(localeStore, ($locale): UiLocale =>
  ($locale in uiMessages ? $locale : 'en') as UiLocale
);
export const currentUiMessages = derived(uiLocale, ($uiLocale) => uiMessages[$uiLocale]);
export const localeOptions = officialStatLocales.map((value) => ({
  value,
  label: localeLabels[value]
}));

export const initializeLocale = () => {
  if (!isBrowser) {
    return;
  }

  const stored = normalizeLocale(localStorage.getItem(localeStorageKey));
  localeStore.set(stored ?? detectLocaleFromNavigator());
};

export const setLocale = (nextLocale: OfficialStatLocale) => {
  localeStore.set(nextLocale);
  if (isBrowser) {
    localStorage.setItem(localeStorageKey, nextLocale);
  }
};

export const getCurrentLocale = (): OfficialStatLocale => get(localeStore);

export const getLocaleLabel = (localeValue: OfficialStatLocale): string => localeLabels[localeValue] ?? localeValue;

export const translateUi = (
  key: UiMessageKey,
  values?: Record<string, string | number | undefined | null>
): string => {
  const currentUiLocale = get(uiLocale);
  let message: string = uiMessages[currentUiLocale][key] ?? uiMessages.en[key] ?? key;

  if (!values) {
    return message;
  }

  Object.entries(values).forEach(([name, value]) => {
    message = message.replaceAll(`{${name}}`, value === undefined || value === null ? '' : String(value));
  });

  return message;
};

export const shouldShowBilingualStats = (localeValue = getCurrentLocale()): boolean => localeValue !== 'en';
